package com.luffykaiyuan.lifeplatform.po.count;

public class CountDict {

    private String dict;

    private int count;

    public void setCount(int count) {
        this.count = count;
    }

    public void setDict(String dict) {
        this.dict = dict;
    }

    public int getCount() {
        return count;
    }

    public String getDict() {
        return dict;
    }
}
